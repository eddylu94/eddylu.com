								<h2>How are taxes in the destination?</h2>

								<p>Remote work is usually taxed by physical location. States with no state income tax: Alaska, Florida, Nevada, South Dakota, Texas, Washington, Wyoming.</p>

								<h2>How will the current lease terminate?</h2>

								<p>If the current lease has to be prematurely terminated, what are the additional costs such as loss of deposit?</p>

								<h1>Things to Do</h1>

								<h2>What is there to do in the destination?</h2>

								<p>Common “things to do” may no longer be valid priorities for the destination due to the pandemic. For example, cities should not be ranked based on their nightlife/bars/clubs. Places with great ski resorts may no longer be of interest.</p>

								<h2>Is there a community in the destination?</h2>

								<p>Having a community is quite important. It may no longer be valid for one to seek new friend groups and communities due to the pandemic. Thus, having a community that is already present may be more important than ever.</p>

